#include "fdf.h"

t_camera	make_camera(void)
{
	t_camera	cam;

	cam.fov = 90;
	cam.far = 1000;
	cam.aspect = 1;
	cam.angle = 0;
	cam.x = 0;
	cam.y = 0;
	return (cam);
}
